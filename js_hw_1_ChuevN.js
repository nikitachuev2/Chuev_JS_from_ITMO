let n1 = 2; // коэффициент при x в первом уравнении
let n2 = 3; // коэффициент при y в первом уравнении
let n3 = 5; // свободный член в первом уравнении

let n4 = 1; // коэффициент при x во втором уравнении
let n5 = 2; // коэффициент при y во втором уравнении
let n6 = 4; // свободный член во втором уравнении

// Вычисление детерминанта основной матрицы (D)
let D = n1 * n5 - n2 * n4;

// Если D равно 0, то система либо имеет бесконечно много решений, либо не имеет решений
if (D === 0) {
    console.log("Система уравнений не имеет единственного решения.");
} else {
    // Вычисление детерминантов для x и y
    let Dx = n3 * n5 - n2 * n6; // детерминант для x
    let Dy = n1 * n6 - n3 * n4; // детерминант для y

    // Вычисление корней x и y
    let result1 = Dx / D; // значение x
    let result2 = Dy / D; // значение y

    // Вывод результатов в консоль
    console.log("Решение системы:");
    console.log("x =", result1);
    console.log("y =", result2);
}